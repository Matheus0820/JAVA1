package dominio;

public class Apostadores {
	private String nomeApostador; 
	private int numApostado; 
	private int valorApostado;
	
	
	public String getNomeApostador() {
		return nomeApostador;
	}
	public void setNomeApostador(String nomeApostador) {
		this.nomeApostador = nomeApostador;
	}
	public int getNumApostado() {
		return numApostado;
	}
	public void setNumApostado(int numApostado) {
		this.numApostado = numApostado;
	}
	public int getValorApostado() {
		return valorApostado;
	}
	public void setValorApostado(int valorApostado) {
		this.valorApostado = valorApostado;
	}
	
	
	
	

}
